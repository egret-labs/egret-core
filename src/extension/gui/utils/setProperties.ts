module egret.gui {

    /**
     * 设置键值对的简便方法。此方法仅供exmlc编译器内部使用。
     */
    export function setProperties(target:any,keys:Array<string>,values:Array<string>):any{

        var length:number = keys.length;
        for(var i:number=0;i<length;i++){
            var key:string = keys[i];
            target[key] = values[i];
        }
        return target;
    }
}